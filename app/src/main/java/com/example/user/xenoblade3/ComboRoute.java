package com.example.user.xenoblade3;

public class ComboRoute {

    int stage1, stage2, stage3;


    public ComboRoute(int s1, int s2, int s3){

        stage1 = s1;
        stage2 = s2;
        stage3 = s3;
    }

    public int getStage1() {
        return stage1;
    }

    public int getStage2() {
        return stage2;
    }

    public int getStage3() {
        return stage3;
    }

}
